// ┌────────────────────────────────────────────────────────────┐
// │  Hyprwright — Config Generator                              │
// │  Turns the app STATE into a valid hyprland.conf string.     │
// └────────────────────────────────────────────────────────────┘
//
// Public API:
//     window.Hypr.generateConfig(state) -> string
//
// `state` has the shape  state[sectionId][fieldKey] = value, matching
// window.Hypr.SCHEMA (see schema.js). This module is the single place
// that knows how to serialize that state into Hyprland syntax.
//
// Design notes:
//   * Never throws — every read is defensive and falls back to the
//     schema default (or a hard-coded fallback if the schema is absent).
//   * Colors in the UI are "#rrggbb"; Hyprland wants rgba(RRGGBBAA).
//   * The `theme` section is preview-only and is emitted only as a
//     trailing comment hint, never as a real config block.

(function () {
  'use strict';

  window.Hypr = window.Hypr || {};

  const INDENT = '    '; // 4 spaces

  // ── Schema-default lookup ─────────────────────────────────────────
  // Build a flat map  "sectionId.fieldKey" -> default  once, so we can
  // resolve missing state values without re-walking the schema each time.
  function buildDefaults() {
    const defs = {};
    const schema = (window.Hypr && window.Hypr.SCHEMA) || [];
    for (const section of schema) {
      if (!section || !Array.isArray(section.fields)) continue;
      for (const field of section.fields) {
        if (!field || typeof field.key !== 'string') continue;
        defs[section.id + '.' + field.key] = field.default;
      }
    }
    return defs;
  }

  // ── Defensive value readers ───────────────────────────────────────
  // Each reader pulls state[section][key]; if absent/blank it falls back
  // to the schema default, then to the supplied hard fallback.
  function makeReader(state, defaults) {
    const get = (section, key, fallback) => {
      const sec = state && state[section];
      let v = sec ? sec[key] : undefined;
      if (v === undefined || v === null || v === '') {
        v = defaults[section + '.' + key];
      }
      if (v === undefined || v === null || v === '') {
        v = fallback;
      }
      return v;
    };

    return {
      raw: get,
      num(section, key, fallback) {
        const n = Number(get(section, key, fallback));
        return Number.isFinite(n) ? n : Number(fallback) || 0;
      },
      bool(section, key, fallback) {
        const v = get(section, key, fallback);
        return v === true || v === 'true' || v === 1 || v === '1';
      },
      str(section, key, fallback) {
        return String(get(section, key, fallback));
      },
      color(section, key, fallback) {
        return hexToRgba(get(section, key, fallback));
      }
    };
  }

  // ── Formatting helpers ────────────────────────────────────────────

  // "#33ccff" -> "rgba(33ccffff)"  (alpha forced to ff)
  function hexToRgba(hex) {
    let h = String(hex == null ? '' : hex).trim();
    if (h.startsWith('#')) h = h.slice(1);
    // Accept 3-digit shorthand too, just in case.
    if (/^[0-9a-fA-F]{3}$/.test(h)) {
      h = h.split('').map((c) => c + c).join('');
    }
    if (!/^[0-9a-fA-F]{6}$/.test(h)) {
      h = '000000'; // last-resort fallback for malformed input
    }
    return 'rgba(' + h.toLowerCase() + 'ff)';
  }

  // Print a float with up to 2 decimals, trimming trailing zeros but
  // keeping at least one decimal place (e.g. 1 -> "1.0", 0.92 -> "0.92",
  // 0.5 -> "0.5", 0.3 -> "0.3").
  function fmtFloat(n) {
    const num = Number(n);
    if (!Number.isFinite(num)) return '0.0';
    let s = num.toFixed(2);
    s = s.replace(/0+$/, '');   // trim trailing zeros
    if (s.endsWith('.')) s += '0'; // keep one decimal place
    return s;
  }

  // Bezier definitions keyed by the animation curve preset.
  const CURVE_BEZIERS = {
    smooth: '0.05, 0.9, 0.1, 1.05',
    snappy: '0.2, 0.9, 0.1, 1.0',
    bouncy: '0.34, 1.56, 0.64, 1.0',
    linear: '0.0, 0.0, 1.0, 1.0'
  };

  // ── Section builders ──────────────────────────────────────────────
  // Each returns an array of lines (no surrounding blank lines).

  function header() {
    return [
      '# ┌─────────────────────────────────────────┐',
      '# │  Generated by Hyprwright                │',
      '# │  https://wiki.hypr.land                 │',
      '# └─────────────────────────────────────────┘'
    ];
  }

  function generalBlock(r) {
    const c1 = r.color('general', 'active_border_1', '#33ccff');
    const c2 = r.color('general', 'active_border_2', '#00ff99');
    const angle = r.num('general', 'active_border_angle', 45);
    const inactive = r.color('general', 'inactive_border', '#1a2030');
    const layout = r.str('general', 'layout', 'dwindle');

    return [
      'general {',
      INDENT + 'gaps_in = ' + r.num('general', 'gaps_in', 5),
      INDENT + 'gaps_out = ' + r.num('general', 'gaps_out', 20),
      INDENT + 'border_size = ' + r.num('general', 'border_size', 2),
      INDENT + 'col.active_border = ' + c1 + ' ' + c2 + ' ' + angle + 'deg',
      INDENT + 'col.inactive_border = ' + inactive,
      INDENT + 'layout = ' + layout,
      INDENT + 'resize_on_border = ' + r.bool('general', 'resize_on_border', true),
      '}'
    ];
  }

  function decorationBlock(r) {
    return [
      'decoration {',
      INDENT + 'rounding = ' + r.num('decoration', 'rounding', 10),
      INDENT + 'active_opacity = ' + fmtFloat(r.num('decoration', 'active_opacity', 1)),
      INDENT + 'inactive_opacity = ' + fmtFloat(r.num('decoration', 'inactive_opacity', 0.92)),
      INDENT + 'dim_inactive = ' + r.bool('decoration', 'dim_inactive', false),
      INDENT + 'dim_strength = ' + fmtFloat(r.num('decoration', 'dim_strength', 0.3)),
      '',
      INDENT + 'blur {',
      INDENT + INDENT + 'enabled = ' + r.bool('decoration', 'blur_enabled', true),
      INDENT + INDENT + 'size = ' + r.num('decoration', 'blur_size', 8),
      INDENT + INDENT + 'passes = ' + r.num('decoration', 'blur_passes', 3),
      INDENT + '}',
      '',
      INDENT + 'shadow {',
      INDENT + INDENT + 'enabled = ' + r.bool('decoration', 'shadow_enabled', true),
      INDENT + INDENT + 'range = ' + r.num('decoration', 'shadow_range', 24),
      INDENT + INDENT + 'color = ' + r.color('decoration', 'shadow_color', '#000000'),
      INDENT + '}',
      '}'
    ];
  }

  function animationsBlock(r) {
    const enabled = r.bool('animations', 'enabled', true);
    const curve = r.str('animations', 'curve', 'smooth');
    const bezier = CURVE_BEZIERS[curve] || CURVE_BEZIERS.smooth;
    // Speed is in deciseconds; clamp to a sane minimum of 1.
    let speed = Math.round(r.num('animations', 'speed', 5));
    if (speed < 1) speed = 1;
    const borderSpeed = Math.max(speed, 10);

    return [
      'animations {',
      INDENT + 'enabled = ' + enabled,
      '',
      INDENT + 'bezier = ease, ' + bezier,
      '',
      INDENT + 'animation = windows, 1, ' + speed + ', ease',
      INDENT + 'animation = windowsOut, 1, ' + speed + ', ease, popin 80%',
      INDENT + 'animation = border, 1, ' + borderSpeed + ', ease',
      INDENT + 'animation = fade, 1, ' + speed + ', ease',
      INDENT + 'animation = workspaces, 1, ' + speed + ', ease',
      '}'
    ];
  }

  function dwindleBlock(r) {
    return [
      'dwindle {',
      INDENT + 'pseudotile = ' + r.bool('dwindle', 'pseudotile', true),
      INDENT + 'preserve_split = ' + r.bool('dwindle', 'preserve_split', true),
      INDENT + 'force_split = ' + r.num('dwindle', 'force_split', 0),
      '}'
    ];
  }

  function masterBlock(r) {
    // The schema only ships dwindle-style fields, but when the user selects
    // the master layout we still emit a valid master block with sensible
    // defaults so the generated config stays internally consistent.
    return [
      'master {',
      INDENT + 'new_status = master',
      INDENT + 'new_on_top = false',
      INDENT + 'mfact = 0.55',
      '}'
    ];
  }

  function themeHint(r) {
    const wallpaper = r.str('theme', 'wallpaper', 'aurora');
    return [
      '# ── Theme / wallpaper ─────────────────────────────────────',
      '# Wallpaper preset in the preview: "' + wallpaper + '"',
      '# The preview theme is cosmetic only and is NOT real config.',
      '# Set a real wallpaper with hyprpaper / swww, e.g.:',
      '# exec-once = hyprpaper'
    ];
  }

  // ── Public entry point ────────────────────────────────────────────
  window.Hypr.generateConfig = function generateConfig(state) {
    try {
      const safeState = state && typeof state === 'object' ? state : {};
      const defaults = buildDefaults();
      const r = makeReader(safeState, defaults);

      const layout = r.str('general', 'layout', 'dwindle');
      const layoutBlock = layout === 'master' ? masterBlock(r) : dwindleBlock(r);

      // Stitch sections together with a single blank line between blocks.
      const blocks = [
        header(),
        generalBlock(r),
        decorationBlock(r),
        animationsBlock(r),
        layoutBlock,
        themeHint(r)
      ];

      const out = [];
      blocks.forEach((block, i) => {
        if (i > 0) out.push(''); // blank line separator
        for (const line of block) out.push(line);
      });

      return out.join('\n') + '\n';
    } catch (err) {
      // Contract: never throw. Surface the failure inside the output so
      // it is visible to the user instead of crashing the app.
      return (
        '# Hyprwright: failed to generate config.\n' +
        '# ' + String(err && err.message ? err.message : err) + '\n'
      );
    }
  };
})();
